import turtle as u
s=u.Screen()
t=u.Turtle()
s.bgcolor("black")
t.pensize(4)
t.pencolor("yellow")
t.speed(1)
for i in range(4):
    t.fd(100)
    t.lt(90)
t.penup()
t.fd(50)
t.lt(90)
t.fd(50)
t.rt(90)
t.pendown()
for i in range(4):
    t.fd(100)
    t.lt(90)
t.goto(0,0)
t.penup()
t.goto(100,0)
t.pendown()
t.goto(150,50)
t.penup()
t.goto(100,100)
t.pendown()
t.goto(150,150)
t.penup()
t.goto(0,100)
t.pendown()
t.goto(50,150)
u.done()
